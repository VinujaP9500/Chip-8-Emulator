#include "chip8.h"
#include<string.h>
#include<stdio.h>

const uint8_t chip8_fontset[80] = {
    0xF0, 0x90, 0x90, 0x90, 0xF0, 
    0x20, 0x60, 0x20, 0x20, 0x70, 
    0xF0, 0x10, 0xF0, 0x80, 0xF0, 
    0xF0, 0x10, 0xF0, 0x10, 0xF0, 
    0x90, 0x90, 0xF0, 0x10, 0x10, 
    0xF0, 0x80, 0xF0, 0x10, 0xF0, 
    0xF0, 0x80, 0xF0, 0x90, 0xF0, 
    0xF0, 0x10, 0x20, 0x40, 0x40, 
    0xF0, 0x90, 0xF0, 0x90, 0xF0, 
    0xF0, 0x90, 0xF0, 0x10, 0xF0, 
    0xF0, 0x90, 0xF0, 0x90, 0x90, 
    0xE0, 0x90, 0xE0, 0x90, 0xE0, 
    0xF0, 0x80, 0x80, 0x80, 0xF0,
    0xE0, 0x90, 0x90, 0x90, 0xE0, 
    0xF0, 0x80, 0xF0, 0x80, 0xF0, 
    0xF0, 0x80, 0xF0, 0x80, 0x80  
};

void chip8_init(Chip8 *cpu) {
	memset(cpu, 0, sizeof(Chip8));
	cpu->pc = 0x200;
	for (int i=0; i<80; i++) {
		cpu->memory[0x50 + i] = chip8_fontset[i];
	}
}

bool chip8_load_rom(Chip8 *cpu, const char *filename) {
	FILE *file = fopen(filename, "rb");
	if (file == NULL) {
		printf("Error: Could not open ROM file: %s\n", filename);
		return false;
	}

	fseek(file, 0, SEEK_END);
	long rom_size = ftell(file);
	fseek(file, 0, SEEK_SET);

	if (rom_size > (4096 - 0x200)) {
		printf("Error: ROM too large to fit in Chip-8 memory.\n");
		fclose(file);
		return false;
	}

	fread(&cpu->memory[0x200], 1, rom_size, file);
	fclose(file);
	printf("Successfully loaded %ld bytes into memory!\n", rom_size);
	return true;
}
